/**
 * Ptslideshow helper.
 */
class PtslideshowHelper
{
	/**
	 * Configure the Linkbar.
	 */
	public static function addSubmenu($vName = '')
	{

		JSubMenuHelper::addEntry(
			JText::_('COM_PTSLIDESHOW_TITLE_SLIDESHOWS'),
			'index.php?option=com_ptslideshow&view=slideshows',
			$vName == 'slideshows'
		);
		JSubMenuHelper::addEntry(
			JText::_('COM_PTSLIDESHOW_TITLE_CATEGORIES'),
			'index.php?option=com_categories&view=categories&extension=com_ptslideshow',
			$vName == 'categories'
		);

	}

	/**
	 * Gets a list of the actions that can be performed.
	 *
	 * @return	JObject
	 * @since	1.6
	 */
	public static function getActions()
	{
		$user	= JFactory::getUser();
		$result	= new JObject;

		$assetName = 'com_ptslideshow';

		$actions = array(
			'core.admin', 'core.manage', 'core.create', 'core.edit', 'core.edit.own', 'core.edit.state', 'core.delete'
		);

		foreach ($actions as $action) {
			$result->set($action,	$user->authorise($action, $assetName));
		}

		return $result;
	}
}
